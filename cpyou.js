// Instruction set:
//
// MOV X Y -> Moves the contents of X to Y
// ADD     -> Adds A and B and puts the result in R
// SUB     -> Subtracts A and B and puts the result in R
// PUT     -> Puts a value in the stack
// POP     -> Gets a value from the stack
// CMP     -> Compares A and B
// RPT     -> Repeats some instructions in the history and decreases counter until counter is 0
// SHL     -> Shifts bits to the left
// SHR     -> Shifts bits to the right
// NOP     -> Does nothing

